#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	i = 0;
	if (dst == NULL || src == NULL)
		return (0);
	while (src[i] && i + 1 < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize && !(dst == NULL && src == NULL))
		dst[i++] = 0;
	if (i == dstsize)
		return (ft_strlen(src));
	return (i - 1);
}
